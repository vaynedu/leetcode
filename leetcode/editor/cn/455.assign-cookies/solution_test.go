func findContentChildren(g []int, s []int) int {
	if len(g) == 0 || len(s) == 0 {
		return 0
	}

	ans := 0
	sort.Ints(g)
	sort.Ints(s)

	for i, j := len(s)-1, len(g)-1; i >= 0 && j >= 0; {
		if s[i] >= g[j] {
			ans++
			j--
			i--
		} else {
			j--
		}

	}
	return ans

}

